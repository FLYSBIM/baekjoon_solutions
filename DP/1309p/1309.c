#include <stdio.h>

int main(void){
	int N=0;
	scanf("%d",&N);
	
	int dp[100001][3]={0};
	dp[1][0]=1;
	dp[1][1]=1;
	dp[1][2]=1;
	dp[2][0]=dp[1][0]+dp[1][1]+dp[1][2];
	dp[2][1]=dp[1][0]+dp[1][2];
	dp[2][2]=dp[1][0]+dp[1][1];

	for(int i=3;i<=N;i++){
		dp[i][0]=dp[i-1][0]%9901+dp[i-1][1]%9901+dp[i-1][2]%9901;
		dp[i][1]=dp[i-1][0]%9901+dp[i-1][2]%9901;
		dp[i][2]=dp[i-1][0]%9901+dp[i-1][1]%9901;
	}
	
	printf("%d\n",(dp[N][0]%9901+dp[N][1]%9901+dp[N][2]%9901)%9901);
}


	
	

	
